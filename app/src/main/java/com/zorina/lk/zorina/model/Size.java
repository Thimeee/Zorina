package com.zorina.lk.zorina.model;

public class Size {

    private int xxl;
    private int xl;
    private int L;
    private int M;
    private int S;

    public Size(){}


    public int getXxl() {
        return xxl;
    }

    public void setXxl(int xxl) {
        this.xxl = xxl;
    }

    public int getXl() {
        return xl;
    }

    public void setXl(int xl) {
        this.xl = xl;
    }

    public int getL() {
        return L;
    }

    public void setL(int l) {
        L = l;
    }

    public int getM() {
        return M;
    }

    public void setM(int m) {
        M = m;
    }

    public int getS() {
        return S;
    }

    public void setS(int s) {
        S = s;
    }
}
